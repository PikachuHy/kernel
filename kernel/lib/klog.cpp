void klog_init(limine_framebuffer* framebuffer) {
    if (framebuffer) {
        fb.addr = (uint8_t*)framebuffer->address;
        fb.width = framebuffer->width;
        fb.height = framebuffer->height;
        fb.pitch = framebuffer->pitch;
        fb.bpp = framebuffer->bpp;
    }
}

